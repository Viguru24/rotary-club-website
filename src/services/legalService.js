export const getLegals = () => {
    const stored = localStorage.getItem(LEGAL_STORAGE_KEY);
    if (!stored) {
        localStorage.setItem(LEGAL_STORAGE_KEY, JSON.stringify(DEFAULT_LEGALS));
        return DEFAULT_LEGALS;
    }
    return JSON.parse(stored);
};

export const saveLegals = (legals) => {
    localStorage.setItem(LEGAL_STORAGE_KEY, JSON.stringify(legals));
    // Dispatch event for components to listen to updates if needed
    window.dispatchEvent(new Event('legals-updated'));
};

export const getLegalDoc = (key) => {
    const legals = getLegals();
    return legals[key] || { title: "Not Found", content: "<p>Document not found.</p>" };
};
